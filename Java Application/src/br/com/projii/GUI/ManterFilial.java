    public void atualizarJTFilial() {
        try {
            if (filialController == null) {
                filialController = new FilialController();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao conectar com o servidor...");
            return;
        }
        Filial[] filiais = filialController.findAll().toArray(new Filial[0]);
        Object[][] objects = new Object[filiais.length][4];
        for (int i = 0; i < filiais.length; i++) {
            objects[i][0] = filiais[i].getId();
            objects[i][1] = filiais[i].getNome();
            objects[i][2] = filiais[i].getEndereco();
            objects[i][3] = filiais[i].getCEP();
        }
        jTFilial.setModel(new javax.swing.table.DefaultTableModel(
                objects,
                new String[]{
                    "id", "Nome", "Endereco", "CEP"
                }));
    }

