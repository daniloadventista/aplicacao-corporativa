    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        Filial filial = null;
        try {
            filial = new Filial(jTFNome.getText());
            filial.setCEP(Long.parseLong(jTFCEP.getText()));
            filial.setEndereco(jTFEndereco.getText());
            try {
                if (filialController == null) {
                    filialController = new FilialController();
                }
                filialController.create(filial);
                atualizarJTFilial();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao gravar Filial: " + filial);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro Formulario mal preenchido",
                    "System Mack", 2);
        }

        limpaTxts();
    }//GEN-LAST:event_jBCadastrarActionPerformed

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        try {
            if (filialController == null) {
                filialController = new FilialController();
            }
            Object id = jTFilial.getValueAt(jTFilial.getSelectedRow(), 0);
            Filial filial = filialController.find(id);
            filialController.delete(filial);
            atualizarJTFilial();
            limpaTxts();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao deletar o Filial");
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

    private void jBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarActionPerformed
        Filial filial = null;
        try {
            if (filialController == null) {
                filialController = new FilialController();
            }
            Object id = jTFilial.getValueAt(jTFilial.getSelectedRow(), 0);
            filial = filialController.find(id);
            filial.setNome(jTFNome.getText());
            filial.setCEP(Long.parseLong(jTFCEP.getText()));
            filial.setEndereco(jTFEndereco.getText());
            
            try {
                filialController.update(filial);
                atualizarJTFilial();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao Alterar o usuario");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro Formulario mal preenchido",
                    "System Mack", 2);
        }
        limpaTxts();
    }//GEN-LAST:event_jBAlterarActionPerformed

